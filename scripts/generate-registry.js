#!/usr/bin/env node

/**
 * Registry Generation Script
 *
 * Scans src/app/pages/<name>/meta.json files and generates a typed TypeScript
 * registry at src/app/config/prototypes.registry.ts.
 *
 * Usage: node scripts/generate-registry.js
 *        yarn generate-registry
 */

const fs = require('fs');
const path = require('path');

const PAGES_DIR = path.join(__dirname, '..', 'src', 'app', 'pages');
const OUTPUT_FILE = path.join(__dirname, '..', 'src', 'app', 'config', 'prototypes.registry.ts');

const REQUIRED_FIELDS = [
  'title',
  'module',
  'slug',
  'route',
  'description',
  'profiles',
  'owner',
  'dateAdded',
  'thumbnail',
];

function findMetaJsonFiles(dir) {
  const results = [];
  const entries = fs.readdirSync(dir, { withFileTypes: true });

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...findMetaJsonFiles(fullPath));
    } else if (entry.name === 'meta.json') {
      results.push(fullPath);
    }
  }

  return results;
}

function validateMeta(meta) {
  const missing = [];
  for (const field of REQUIRED_FIELDS) {
    if (!(field in meta)) {
      missing.push(field);
    }
  }
  return missing;
}

function safeStringify(value) {
  return JSON.stringify(value);
}

function inferArchitecture(filePath) {
  const normalized = filePath.split(path.sep).join('/');
  if (normalized.includes('/pages/old/')) return 'old';
  if (normalized.includes('/pages/new/') || normalized.includes('/pages/gali-v5/')) return 'new';
  return 'old';
}

function enrichMeta(meta, filePath) {
  const enriched = { ...meta };
  if (!enriched.architecture) {
    enriched.architecture = inferArchitecture(filePath);
  }
  return enriched;
}

function formatEntry(meta) {
  const lines = [
    '  {',
    `    title: ${safeStringify(meta.title)},`,
    `    module: ${safeStringify(meta.module)},`,
    `    slug: ${safeStringify(meta.slug)},`,
    `    route: ${safeStringify(meta.route)},`,
    `    description: ${safeStringify(meta.description)},`,
    `    profiles: ${safeStringify(meta.profiles)},`,
    `    owner: ${safeStringify(meta.owner)},`,
    `    dateAdded: ${safeStringify(meta.dateAdded)},`,
    `    thumbnail: ${safeStringify(meta.thumbnail)},`,
    `    architecture: ${safeStringify(meta.architecture)},`,
  ];

  if (meta.folder) {
    lines.push(`    folder: ${safeStringify(meta.folder)},`);
  }
  if (meta.absoluteRoute) {
    lines.push(`    absoluteRoute: ${safeStringify(meta.absoluteRoute)},`);
  }

  lines.push('  }');
  return lines.join('\n');
}

const metaFiles = findMetaJsonFiles(PAGES_DIR);

if (metaFiles.length === 0) {
  console.error('ERROR: No meta.json files found in src/app/pages/');
  process.exit(1);
}

const entries = [];
let hasErrors = false;

for (const filePath of metaFiles) {
  let raw;
  try {
    raw = fs.readFileSync(filePath, 'utf-8');
  } catch (err) {
    console.error(`ERROR: Cannot read file ${filePath}: ${err.message}`);
    hasErrors = true;
    continue;
  }

  let meta;
  try {
    meta = JSON.parse(raw);
  } catch (err) {
    console.error(`ERROR: Invalid JSON in ${filePath}: ${err.message}`);
    hasErrors = true;
    continue;
  }

  const missing = validateMeta(meta);
  if (missing.length > 0) {
    console.error(`ERROR: ${filePath} is missing required fields: ${missing.join(', ')}`);
    hasErrors = true;
    continue;
  }

  entries.push(enrichMeta(meta, filePath));
}

if (hasErrors) {
  console.error('\nRegistry generation failed due to validation errors.');
  process.exit(1);
}

entries.sort((a, b) => {
  const moduleCompare = a.module.localeCompare(b.module);
  if (moduleCompare !== 0) return moduleCompare;
  return a.title.localeCompare(b.title);
});

const tsContent = [
  '// AUTO-GENERATED by scripts/generate-registry.js',
  '// Do not edit manually. Run: yarn generate-registry',
  "import { PrototypeMeta } from '../models/prototype-meta.model';",
  '',
  'export const PROTOTYPE_REGISTRY: PrototypeMeta[] = [',
  entries.map(formatEntry).join(',\n'),
  '];',
  '',
].join('\n');

const outputDir = path.dirname(OUTPUT_FILE);
if (!fs.existsSync(outputDir)) {
  fs.mkdirSync(outputDir, { recursive: true });
}

fs.writeFileSync(OUTPUT_FILE, tsContent, 'utf-8');

console.log(`Generated prototypes.registry.ts with ${entries.length} prototypes`);
